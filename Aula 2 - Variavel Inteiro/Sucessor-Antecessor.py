# Solicita que o usuário digite um número e converte para ponto flutuante
numero = float(input("Digite um número: "))

# Calcula o sucessor
sucessor = numero + 1

# Calcula o antecessor
antecessor = numero - 1

# Exibe o sucessor
print("O Sucessor do numero", numero, "é o numero", sucessor)

# Exibe o dobro do número
print("O Antecessor do numero", numero, "é o numero", antecessor)